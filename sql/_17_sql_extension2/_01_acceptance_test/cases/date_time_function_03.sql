--WEEKDAY FUNCTION
SELECT WEEKDAY('2010-09-20 11:32:24');
SELECT WEEKDAY('2010/09/21');
SELECT WEEKDAY(TIME'2010-09-22 11:33:56');
SELECT WEEKDAY(DATETIME'2010-09-23 22:39:56.1234');
SELECT WEEKDAY(TIMESTAMP'2010-09-24 23:59:59');

