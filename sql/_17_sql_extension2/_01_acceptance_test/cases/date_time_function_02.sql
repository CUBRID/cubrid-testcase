--WEEK FUNCTION
--52
SELECT WEEK('1999-12-30');
--7
SELECT WEEK('2008-02-20 01:02:03.1234');
--22
SELECT WEEK(DATE'2010-05-30', 4);
SELECT WEEK(DATETIME'2010-10-01 22:39:56.1234', 0);
--39
SELECT WEEK(TIMESTAMP'2010-10-01 23:59:59', 6);

