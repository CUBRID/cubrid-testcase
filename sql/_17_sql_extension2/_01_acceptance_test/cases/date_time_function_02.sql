--HOUR FUNCTION
SELECT HOUR('11:32:24');
SELECT HOUR('04/23/1999 22:10:54');
SELECT HOUR(TIME'2010-10-01 11:33:56');
SELECT HOUR(DATETIME'2010-10-01 22:39:56.1234');
SELECT HOUR(TIMESTAMP'2010-10-01 23:59:59');

