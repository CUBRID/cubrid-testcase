--create a serial with a very long minus data like 'START WITH -10000000000000000000000000000000000' and 'MINVALUE -10000000000000000000000000000000000' then drop the serial

CREATE SERIAL ddl_0001_serial
START WITH -10000000000000000000000000000000000
INCREMENT BY -1
MINVALUE -10000000000000000000000000000000000
maxVALUE 1000000
CYCLE;





DROP SERIAL ddl_0001_serial;